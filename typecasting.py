# TYPECASTING IN PYTHON

a = "1"
b= "2"

print(a+b)  #As a and b are strings so the output is 12

# EXPLICIT TYPECASTING 

a = int(a)  # typecasting - converting string to integar
b = int(b)  # typecasting - converting string to integar

print(a+b)  # now a and b are integar


