name = "sonu, putai,"

# slicing - index 0 to 7
print(name[0:8])
print(name[:4])

print(len(name))  #len is a in-built functions of python that outputs length of a string

print(name[:])  #in this case full string will printed.....
print(name[0:-4])  # this mean print(name[0:len(name)-4])

print(name[-3:-1])   # len(name) -3 to len(name) - 1  


